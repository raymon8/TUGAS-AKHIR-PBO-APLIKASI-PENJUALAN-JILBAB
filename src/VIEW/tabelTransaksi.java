private void showtable()
    {
        cnn.koneksiDataBase();
        DefaultTableModel tabel = new DefaultTableModel();
        
        tabel.addColumn("ID");
        tabel.addColumn("Kode Jilbab");
        tabel.addColumn("Harga Satuan");
        tabel.addColumn("Jumlah Beli");
        tabel.addColumn("Keterangan");
        tabel.addColumn("Total Bayar");
        tabel.addColumn("Jumlah Bayar");
        tabel.addColumn("Kembalian");
        jTable1.setModel(tabel);
        
        try 
        {
            String sql = "select * from tbl_transaksi";

            ResultSet rs = cnn.st.executeQuery(sql);
            while (rs.next()) 
            {
                tabel.addRow(new Object[]
                {
                rs.getString("idTransaksi"),
                rs.getString("KodeJilbab"),
                rs.getString("HargaSatuan"),
                rs.getString("JumlahBeli"),
                rs.getString("Keterangan"),
                rs.getString("TotalBayar"),
                rs.getString("JumlahBayar"),
                rs.getString("Kembalian")
                });
                jTable1.setModel(tabel);
            } 
        }
        catch (SQLException e) 
        {
            System.err.println("Pembeli View Showtable Error" + e);
        }
    }
