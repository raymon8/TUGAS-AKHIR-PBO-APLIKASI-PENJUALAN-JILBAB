public class PembeliModel {
      private String KodeKasir,NamaKasir,NomorTeleponKasir,AlamatKasir;
    private int idKasir;

    public String getKodeKasir() {
        return KodeKasir;
    }

    public void setKodeKasir(String KodeKasir) {
        this.KodeKasir = KodeKasir;
    }

    public String getNamaKasir() {
        return NamaKasir;
    }

    public void setNamaKasir(String NamaKasir) {
        this.NamaKasir = NamaKasir;
    }

    public String getNomorTeleponKasir() {
        return NomorTeleponKasir;
    }

    public void setNomorTeleponKasir(String NomorTeleponKasir) {
        this.NomorTeleponKasir = NomorTeleponKasir;
    }

    public String getAlamatKasir() {
        return AlamatKasir;
    }

    public void setAlamatKasir(String AlamatKasir) {
        this.AlamatKasir = AlamatKasir;
    }

    public int getIdKasir() {
        return idKasir;
    }

    public void setIdKasir(int idKasir) {
        this.idKasir = idKasir;
    }
    
}